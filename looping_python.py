# looping_python.py

# Looping menggunakan for
print("Looping for:")
for i in range(1, 6):
    print("Angka:", i)

# Looping list
buah = ["apel", "pisang", "mangga"]
print("\nLooping list:")
for b in buah:
    print(b)

# Looping menggunakan while
print("\nLooping while:")
x = 1
while x <= 3:
    print("Nilai x:", x)
    x += 1
