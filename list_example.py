# list_example.py

# Membuat list
buah = ["apel", "pisang", "mangga"]

# Menampilkan list
print("Daftar buah:", buah)

# Menambah data
buah.append("jeruk")
print("Setelah ditambah:", buah)

# Mengakses elemen list
print("Buah pertama:", buah[0])

# Menghapus data
buah.remove("pisang")
print("Setelah dihapus:", buah)
